const { db } = require("../models/conexion.model");





///////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////               SELECT TURNOS  ALL            /////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
exports.findAllTurnos = async(req, res) => { 
    const Turnos = await db.tb_turnos.findAll({
        order: [ ['id_turno', 'DESC'] ],
    });
    res.send(Turnos);
}


///////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////              BUSCAR TURNO ABIERTO           /////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
exports.findOneTurno = async(req, res) => { 
    const Turnos = await db.tb_turnos.findOne({
        where: { estado: 0 }
    });
    res.send(Turnos);
}